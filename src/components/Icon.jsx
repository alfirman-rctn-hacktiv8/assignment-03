export const Theme = ({ isDark, setDark }) => {
  const togleTheme = () => {
    const body = document.querySelector("body");
    if (isDark) {
      body.removeAttribute("class");
      setDark(false);
    } else {
      body.classList.add("dark");
      setDark(true);
    }
  };
  return (
    <button onClick={() => togleTheme()} className="relative dark:invert">
      <i
        className={`${
          !isDark
            ? "rotate-0 opacity-100 delay-500"
            : "rotate-[360deg] opacity-0 invisible"
        } transform duration-500 absolute right-0 top-3`}
      >
        <Moon />
      </i>
      <i
        className={`${
          isDark
            ? "rotate-0 opacity-100 delay-500"
            : "rotate-[-360deg] opacity-0 invisible"
        } transform duration-500 absolute right-0 top-3`}
      >
        <Sun />
      </i>
    </button>
  );
};
